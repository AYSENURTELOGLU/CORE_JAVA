package d05_matematikselislemler_increment;

public class c04_preincrement_postincremet {

    public static void main(String[] args) {

        //java da bir önceki class da yaptıgımız gibi artırma ve atama veya artırma ve yazdırma
        //beraber olabilir



        /*
        önce artırma ++a, önce atama sonra arttırma a++ olur.
         */

        int a=20;
        System.out.println(a++); //20
        System.out.println(a); //21

        a=20;
        System.out.println(--a); //19
        System.out.println(a); //19





    }
}
